def search_tickers(query: str, limit: int = 10) -> list[dict[str, str]]:
    """Search tickers by ticker symbol or company name (case-insensitive)."""
    query = query.upper().strip()
    if not query:
        return []

    results = []
    # Exact ticker match first
    if query in ALL_TICKERS:
        results.append({"ticker": query, "name": ALL_TICKERS[query]})

    # Prefix match on ticker
    for ticker, name in ALL_TICKERS.items():
        if ticker.startswith(query) and ticker != query:
            results.append({"ticker": ticker, "name": name})

    # Substring match on ticker or company name
    for ticker, name in ALL_TICKERS.items():
        if ticker == query or ticker.startswith(query):
            continue  # Already added
        if query in ticker or query in name.upper():
            results.append({"ticker": ticker, "name": name})

    return results[:limit]
